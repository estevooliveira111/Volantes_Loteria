/*função da Base de Dados Mega Sena*/

function xum(ll) {
    var display = document.getElementById(ll).style.display;
    if (display == "nome")
        document.getElementById(ll).style.display = 'block';
    else
        document.getElementById(ll).style.display = 'nome'

}

